""" 
Convert a string to an integer and vice versa.
"""

# Convert string to integer
string_num = "556"
int_num = int(string_num)
print("String to Integer:", int_num)
print("Type after conversion:", type(int_num))

# Convert integer to string
num = 456
str_num = str(num)
print("\nInteger to String:", str_num)
print("Type after conversion:", type(str_num))